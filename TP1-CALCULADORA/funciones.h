/** \brief Pide que el usuario ingrese un numero por teclado
 *
 * \param void EL valor que toma la funcion es void
 * \return int EL valor que retorna la funcion ingresado por el usuario
 *
 */
float enterNumber(void);

/** \brief Realiza la suma de los operandos A y B
 *
 * \param number1 int El primer numero a sumar
 * \param number2 int El segundo numero a sumar
 * \return int Devuelv el valor de la suma
 *
 */
float add(float number1, float number2);

/** \brief Realiza la resta de los parametros A y B
 *
 * \param number1 int El valor A a ser restado
 * \param number2 int El valor B que resta al B
 * \return int El resultado de la resta
 *
 */
float substraction(float number1, float number2);

/** \brief Realiza la multiplicacion de los parametros A y B
 *
 * \param number1 int El valor A, el multiplicando
 * \param number2 int EL valor B, el multiplicador
 * \return float El resultado de la multiplicacion,el producto
 *
 */
float multiplication(float number1, float number2);

/** \brief Realiza la division de los parametros A y B
 *
 * \param number1 int El valor de A a ser dividido por B
 * \param number2 int El valor de B, quien va a dividir A
 * \return float El valor de la division
 *
 */
float divition(float number1, float number2);

/** \brief Realiza el factorial del numero pasado a la funcion
 *
 * \param number int El numero a factorizar
 * \return int El resultado de la factorización
 *
 */
float factorial(float number);
